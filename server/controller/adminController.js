// Assign subject to faculty
export const assignSubjectToFaculty = async (req, res) => {
  try {
    const { facultyId, subjectId, department } = req.body;

    let faculty;
    let subject;

    // Validate inputs
    if (!facultyId || !subjectId || !department) {
      return res.status(400).json({ 
        success: false, 
        message: "Faculty ID, Subject ID and Department are required" 
      });
    }

    // Check if faculty exists
    faculty = await Faculty.findById(facultyId);
    if (!faculty) {
      return res.status(404).json({
        success: false,
        message: "Faculty not found",
      });
    }

    // Check if subject exists
    subject = await Subject.findById(subjectId);
    if (!subject) {
      return res.status(404).json({
        success: false,
        message: "Subject not found",
      });
    }

    // Check if faculty and subject belong to the same department
    if (faculty?.department !== subject?.department) {
      return res.status(400).json({
        success: false,
        message: "Faculty and subject must belong to the same department",
      });
    }
    if (!facultyId || !subjectId || !department) {
      return res.status(400).json({ 
        success: false, 
        message: "Faculty ID, Subject ID and Department are required" 
      });
    }

    // Check if faculty exists
    faculty = await Faculty.findById(facultyId);
    if (!faculty) {
      return res.status(404).json({
        success: false,
        message: "Faculty not found",
      });
    }

    // Check if subject exists
    subject = await Subject.findById(subjectId);
    if (!subject) {
      return res.status(404).json({
        success: false,
        message: "Subject not found",
      });
    }

    // Check if faculty and subject belong to the same department
    if (faculty?.department !== subject?.department) {
      return res.status(400).json({
        success: false,
        message: "Faculty and subject must belong to the same department",
      });
    }

    // Check if assignment already exists
    const existingAssignment = await FacultySubject.findOne({ 
      faculty: facultyId, 
      subject: subjectId 
    });
    
    if (existingAssignment) {
      return res.status(400).json({ 
        success: false, 
        message: "Subject already assigned to this faculty" 
      });
    }
    
    // Create new assignment
    const newAssignment = new FacultySubject({
      faculty: facultyId,
      subject: subjectId,
      department
    });
    
    await newAssignment.save();
    
    res.status(201).json({
      success: true,
      message: "Subject assigned to faculty successfully",
      result: newAssignment
    });
    
  } catch (error) {
    console.log(error);
    res.status(500).json({
      success: false,
      message: "Something went wrong",
      error: error.message
    });
  }
};

